def _post_install_hook_map_eu_taxes(cr, registry):
    env = api.Environment(cr, SUPERUSER_ID, {})
    eu_countries = env.ref("base.europe").country_ids
    companies = env['res.company'].search([])
    for c in companies.filtered(lambda c: c.country_id in eu_countries):
        tax_ids = env['account.tax'].search([('type_tax_use', '=', 'sale'),
            ('amount_type', '=', 'percent'),
            ('company_id','=', c.id)])
        for tax_to_map in tax_ids:
            eu_countries = env.ref("base.europe").country_ids - c.country_id
            for country in eu_countries - c.country_id:
                print('looking for %s' % c.country_id.code + str(tax_to_map.amount) + country.code)
                replacement_tax_amt = _eu_tax_map().get(c.country_id.code + str(tax_to_map.amount) + country.code, tax_to_map.amount)
                eu_tax_name =_('%(label)s in %(country)s (%(rate)s)' % {'label': country.vat_label, 'country': country.name, 'rate':replacement_tax_amt})
                existing_tax = env['account.tax'].search([('name', '=', eu_tax_name)])
                if not existing_tax:
                    existing_tax = env['account.tax'].create({
                        'name': eu_tax_name,
                        'amount': replacement_tax_amt,
                        # 'invoice_repartition_line_ids': self._get_repartition_line_copy_values(tax_to_map.invoice_repartition_line_ids),
                        # 'refund_repartition_line_ids': self._get_repartition_line_copy_values(tax_to_map.refund_repartition_line_ids),
                        'type_tax_use': 'sale',
                        'description': "%s%%" % replacement_tax_amt,
                        'sequence': 1000,
                        'company_id': c.id,
                    })
                fpos = env['account.fiscal.position'].search([('country_id', '=', country.id), ('company_id', '=', c.id)], limit=1)
                if not fpos:
                    new_fpos_name = _('OSS B2C %(country)s' % {'country': country.name})
                    fpos = env['account.fiscal.position'].create({
                        'name': new_fpos_name,
                        'company_id': c.id,
                        'vat_required': False,
                        'auto_apply': True,
                        'country_id': country.id,
                        'account_ids': False,
                        'tax_ids': [(0, 0, {'tax_src_id': tax_to_map.id, 'tax_dest_id': existing_tax.id})]
                    })
                else:
                    fpos.write({
                        'tax_ids': [(0, 0, {'tax_src_id': tax_to_map.id, 'tax_dest_id': existing_tax.id})]
                    })

